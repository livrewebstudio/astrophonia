// Current language (default EN, will be set by splash)
let currentLang = 'en';

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('ap_lang', lang);
  applyTranslations(lang);
  updateNavLangDisplay(lang);

  // Hide splash, show site
  const splash = document.getElementById('splash');
  const wrapper = document.getElementById('site-wrapper');
  if (splash && wrapper) {
    splash.classList.add('hidden');
    setTimeout(() => {
      splash.style.display = 'none';
      wrapper.style.display = 'block';
      revealOnScroll();
    }, 850);
  }

  // Update html lang attribute
  document.documentElement.lang = lang;
}

function applyTranslations(lang) {
  const t = translations[lang] || translations['en'];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) {
      // Avoid overwriting inner HTML with child elements (cards, etc.)
      if (el.children.length === 0) {
        el.textContent = t[key];
      } else {
        // Only update text nodes
        el.childNodes.forEach(node => {
          if (node.nodeType === 3) node.textContent = '';
        });
        el.insertAdjacentText('afterbegin', t[key]);
      }
    }
  });

  // Special: splash subtitle
  const splashSub = document.getElementById('splash-sub');
  if (splashSub) splashSub.textContent = t.splash_sub || '';
  const splashLabel = document.getElementById('splash-lang-label');
  if (splashLabel) splashLabel.textContent = t.splash_lang_label || '';

  // Cookie banner
  const cookieText = document.getElementById('cookie-text');
  if (cookieText) {
    const policyLink = lang === 'pt' ? 'Política de Cookies' : lang === 'it' ? 'Cookie Policy' : 'Cookie Policy';
    cookieText.innerHTML = `${t.cookie_text} <a href="cookies.html">${policyLink}</a>`;
  }
}

function updateNavLangDisplay(lang) {
  const flags = { en: '🇬🇧', it: '🇮🇹', pt: '🇧🇷' };
  const labels = { en: 'EN', it: 'IT', pt: 'PT' };
  const flagEl = document.getElementById('current-flag');
  const labelEl = document.getElementById('current-lang-label');
  if (flagEl) flagEl.textContent = flags[lang] || '🇬🇧';
  if (labelEl) labelEl.textContent = labels[lang] || 'EN';
}

function toggleLangDropdown() {
  const dd = document.getElementById('lang-dropdown');
  if (dd) dd.classList.toggle('open');
}

// Close dropdown on outside click
document.addEventListener('click', (e) => {
  const dd = document.getElementById('lang-dropdown');
  const btn = document.getElementById('lang-toggle-btn');
  if (dd && !dd.contains(e.target) && btn && !btn.contains(e.target)) {
    dd.classList.remove('open');
  }
});

// Init: check saved lang preference
document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('ap_lang');
  if (saved && translations[saved]) {
    // Apply translations to splash too
    applyTranslations(saved);
    // Auto-skip splash if they've been here before
    // (Uncomment below to skip splash for returning visitors)
    // setLang(saved);
  }
});
